const photoViewer = document.getElementById("photoViewer")
const selectedPhoto = document.getElementById("selectedPhoto")

function dontShow() {
    photoViewer.classList.remove("show")
    photoViewer.classList.add("hide")
    selectedPhoto.src="";
}

